package work_qts;

public class subtraiCalculadora {

	int numero,	numero2;
	
	
public int getNumero() {
		return numero;
	}


	public void setNumero(int numero) {
		this.numero = numero;
	}


	public int getNumero2() {
		return numero2;
	}


	public void setNumero2(int numero2) {
		this.numero2 = numero2;
	}


public int subtrair(int numero, int numero2) {
		
		return numero - numero2;
		
	}
	
	
	
}
